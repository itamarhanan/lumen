import { readFile, writeFile } from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";
import { minify } from "terser";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

async function main() {
  const sourcePath = path.join(ROOT, "src/lib/web-source.js");
  const source = await readFile(sourcePath, "utf-8");

  const result = await minify(source, {
    compress: {
      passes: 2,
      dead_code: true,
      toplevel: true,
      unsafe: false,
      switches: true,
      computed_props: true,
    },
    mangle: {
      toplevel: true,
      eval: true,
      properties: {
        reserved: [
          "__LUMEN__",
          "lumen",
          "dataset",
          "siteId",
          "ingestUrl",
          "pushState",
          "popstate",
          "sendBeacon",
          "fetch",
          "method",
          "headers",
          "body",
          "keepalive",
          "currentScript",
        ],
      },
    },
    format: {
      comments: false,
    },
  });

  if (!result.code) {
    console.error("Minification produced no output");
    process.exit(1);
  }

  const template = `// Auto-generated by scripts/build-script.mjs — do not edit
export const SCRIPT_TEMPLATE: string = ${JSON.stringify(result.code)};
`;

  const outPath = path.join(ROOT, "src/lib/script-template.ts");
  await writeFile(outPath, template);
  console.log(`Script template written to ${outPath}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
